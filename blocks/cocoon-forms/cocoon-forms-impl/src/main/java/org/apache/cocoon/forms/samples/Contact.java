package org.apache.cocoon.forms.samples;

/**
 * Contact belongs to the {@link Form2Bean} demo.
 * 
 * @version $Id$
 */
public class Contact {
    private long id;
    private String firstName;
    private String middleInitial;
    private String lastName;
    private String phone;
    private String email;
    private PreferredContact preferred;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String toString() {
        return "< id = " + id + ", firstName = " + firstName + ", middleInitial = " + middleInitial
            + ", lastName = " + lastName + ", phone = " + phone + ", email = " + email + " >";
    }

    public PreferredContact getPreferred() {
        return preferred;
    }

    public void setPreferred(PreferredContact preferred) {
        this.preferred = preferred;
    }

    public String getMiddleInitial() {
        return middleInitial;
    }

    public void setMiddleInitial(String middleInitial) {
        this.middleInitial = middleInitial;
    }
}
